private void cargarTickets() {
    modelotabla.setRowCount(0);
    for (TicketPartidoDTO dto : controlador.obtenerTicketsConPartido()) {
        modelotabla.addRow(new Object[]{
            dto.getTicketId(),    
            dto.getPartidoId(),    
            dto.getEquipoLocal(),    
            dto.getEquipoVisitante(), 
            dto.getFase(),
            dto.getEstadio(),       
            dto.getFecha(),          
            dto.getHora(),            
            dto.getNumeroAsiento(),   
            dto.getSeccion(),        
            dto.getPrecio(),         
            dto.getEstado()         
        });
    }
}

private void cargarPartidos() {
    DefaultTableModel modelo = new DefaultTableModel(
        new Object[]{"ID","Local","Visitante","Fecha","Hora","Fase","Estadio","Ciudad","Capacidad","Estado"}, 0
    );
    jTable1.setModel(modelo);

    PartidoControlador pc = new PartidoControlador();
    for (PartidoModelo p : pc.obtenerPartido()) {
    modelo.addRow(new Object[]{
        p.getId(),
        p.getEquipo_local(),
        p.getEquipo_visitante(),
        p.getFecha(),
        p.getHora(),
        p.getFase(),
        p.getEstadio(),
        p.getCiudad(),
        p.getCapacidad(),
        p.getEstado()
    });
    }
}
