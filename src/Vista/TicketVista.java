    private void btcrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btcrearActionPerformed
    int partido_id = Integer.parseInt(txtpartido.getText());
    String seccion = boxseccion.getSelectedItem().toString();
    String numero_asiento = txtasiento.getText();
    double precio = Double.parseDouble(txtprecio.getText());
    String estado = "DISPONIBLE";
    
    PartidoControlador pc = new PartidoControlador();
    boolean existe = pc.obtenerPartido().stream()
    .anyMatch(p -> p.getId() == partido_id);
    
    if (txtfecha.getText() == null || txtfecha.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar un partido para obtener la fecha");
        return;
    }
LocalDate fechaPartido;
try {
    fechaPartido = LocalDate.parse(txtfecha.getText().trim());
} catch (Exception e) {
    JOptionPane.showMessageDialog(this,
        "Formato de fecha inválido: " + txtfecha.getText());
    return;
}
   boolean resultado = controlador.crearTicket(
    partido_id,
    numero_asiento,
    seccion,
    precio,
    estado,
    fechaPartido.atStartOfDay()
);
    if (resultado) {
        JOptionPane.showMessageDialog(this, "Ingresado correctamente");
    } else {
        JOptionPane.showMessageDialog(this, "No se ingresó correctamente");
    }
    cargarTickets();
   
    }//GEN-LAST:event_btcrearActionPerformed

